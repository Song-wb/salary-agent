# 经验年限 → 相对于中位薪资的比例
EXPERIENCE_MULTIPLIER = {
    "0-1年": 0.50,
    "1-3年": 0.75,
    "3-5年": 1.00,
    "5年+":  1.35,
}


class SalaryBenchmark:
    """薪酬基准数据访问层"""

    @staticmethod
    def get_position_names(position_key: str) -> str:
        """模糊匹配岗位名称"""
        for name in POSITION_SALARIES:
            if position_key in name or name.startswith(position_key[:2]):
                return name
        return position_key

    @staticmethod
    def get_base_salary(position: str) -> dict:
        """获取岗位的全国基准薪资"""
        # 精确匹配
        if position in POSITION_SALARIES:
            return POSITION_SALARIES[position].copy()
        # 模糊匹配
        for name, sal in POSITION_SALARIES.items():
            if name.startswith(position[:2]) or position in name:
                return sal.copy()
        # 默认值
        return {"p25": 12000, "p50": 18000, "p75": 26000, "avg": 19000}

    @staticmethod
    def get_positions_for_industry(industry: str) -> list:
        """获取某行业的典型岗位列表"""
        return INDUSTRY_POSITIONS.get(industry, ["Java开发", "产品经理", "前端开发",
                                                  "数据分析师", "运维工程师"])

    @staticmethod
    def calculate_city_adjusted(position: str, city: str, industry: str) -> dict:
        """计算某岗位在指定城市和行业的薪资"""
        base = SalaryBenchmark.get_base_salary(position)
        cf = CITY_FACTOR.get(city, 0.85)
        ind_f = INDUSTRY_FACTOR.get(industry, 0.90)

        # 城市 + 行业 综合系数
        combined = cf * ind_f

        return {
            "p25": int(base["p25"] * combined / 1000 * 1000),
            "p50": int(base["p50"] * combined / 1000 * 1000),
            "p75": int(base["p75"] * combined / 1000 * 1000),
            "avg": int(base["avg"] * combined / 1000 * 1000),
        }

    @staticmethod
    def get_experience_salaries(base_avg: int) -> list:
        """根据平均薪资估算各经验段薪资"""
        return [
            {"experience": exp, "avg_salary": int(base_avg * mult / 1000 * 1000)}
            for exp, mult in EXPERIENCE_MULTIPLIER.items()
        ]

    @staticmethod
    def get_data_source_description() -> dict:
        """返回数据来源说明"""
        return {
            "source_name": "行业薪酬基准数据库",
            "source_description": "基于国家统计局、智联招聘、BOSS直聘研究院、拉勾网等公开数据综合编制",
            "data_freshness": "2025年第一季度",
            "coverage": "8大行业 × 10城市 × 50+岗位",
        }
